class Operator {

  // メゾット
  public static void main(String[] args) {
    int a = 2;
    int b = 6;
    System.out.println( a + b );
    System.out.println( a - b );
    System.out.println( a * b );
    System.out.println( a / b );
    System.out.println( a % b );
    System.out.println( b = --a );
    System.out.println( a += 2 );
  }
}